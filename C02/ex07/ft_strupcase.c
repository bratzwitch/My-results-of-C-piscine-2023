#include <stdio.h>

char	*ft_strupcase(char *str)
{
	int	i;

	if (str == 0)
	{
		return (0);
	}
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			str[i] -= 32;
		}
		i++;
	}
	return (str);
}
